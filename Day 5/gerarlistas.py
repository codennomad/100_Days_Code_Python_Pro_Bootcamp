import string

# Letras (minúsculas e maiúsculas)
letters_lower = list(string.ascii_lowercase)  # Letras minúsculas
#letters_upper = list(string.ascii_uppercase)  # Letras maiúsculas

# Números (0-9)
numbers = list(string.digits)  # Números de 0 a 9

# Símbolos comuns
symbols = list(string.punctuation)  # Símbolos comuns como !, @, #, etc.

# Exibe as listas
print("Letras minúsculas:", letters_lower)
print("Números:", numbers)
print("Símbolos:", symbols)
